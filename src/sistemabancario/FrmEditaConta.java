    public FrmEditaConta(FrmPrincipal frmPrincipal, FrmCadastroConta frmCadastro, Pessoa pessoa, Conta conta) {
        super(frmPrincipal);
        initComponents();

        validador.add(new ValidadorCampo(txtAgencia, "Agência", ValidadorTipo.NumeroPositivo));
        validador.add(new ValidadorCampo(txtConta, "Nº Conta", ValidadorTipo.NumeroPositivo));
        validador.add(new ValidadorCampo(txtSenha, "Senha", ValidadorTipo.NaoVazio));

        this.frmCadastro = frmCadastro;
        this.titular = pessoa;
        this.conta = conta;

        txtTitular.setText(titular.getNome());
        if (conta != null) {
            this.setTitle("Alteração de Conta");

            txtAgencia.setText(conta.getAgencia());
            txtConta.setText(conta.getNumeroConta());

            txtAgencia.setEnabled(false);
            txtConta.setEnabled(false);

            if (conta.getStatus() == StatusConta.Encerrada)
            {
                txtSenha.setEnabled(false);
                txtConfirmacaoSenha.setEnabled(false);
                btnSalvar.setEnabled(false);
            }
            
            txtSenha.setText(conta.getSenha());
            txtConfirmacaoSenha.setText(conta.getSenha());

            cbTipoConta.setSelectedItem(conta.getTipoConta().toString());
            cbTipoConta.setEnabled(false);

            txtStatusConta.setText(conta.getStatus().toString());

            habilitaCamposConta();

            if (cbTipoConta.getSelectedItem().toString().equals("Poupanca")) {
                txtTaxaRendimento.setText(decimalFormat.format(((ContaPoupanca) conta).getTaxaRendimento()));
            } else if (cbTipoConta.getSelectedItem().toString().equals("Corrente")) {
                txtTaxaMensal.setText(decimalFormat.format(((ContaCorrente) conta).getTaxaMensal()));
            } else {
                txtTaxaMensal.setText(decimalFormat.format(((ContaEspecial) conta).getTaxaMensal()));
                txtTaxaJuros.setText(decimalFormat.format(((ContaEspecial) conta).getTaxaJuros()));
                txtLimiteConta.setText(decimalFormat.format(((ContaEspecial) conta).getLimiteConta()));
            }
        }
    }

    private void habilitaCamposConta() {
        txtTaxaRendimento.setEnabled(false);
        txtTaxaMensal.setEnabled(false);
        txtTaxaJuros.setEnabled(false);
        txtLimiteConta.setEnabled(false);

        if (conta.getStatus() == StatusConta.Ativa) {
            if (cbTipoConta.getSelectedItem().toString().equals("Poupanca")) {
                txtTaxaRendimento.setEnabled(true);
            } else if (cbTipoConta.getSelectedItem().toString().equals("Corrente")) {
                txtTaxaMensal.setEnabled(true);
            } else if (cbTipoConta.getSelectedItem().toString().equals("Especial")) {
                txtTaxaMensal.setEnabled(true);
                txtTaxaJuros.setEnabled(true);
                txtLimiteConta.setEnabled(true);
            }
        }
    }

