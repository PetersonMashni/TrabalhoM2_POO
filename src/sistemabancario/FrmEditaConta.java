    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!validador.validar()) {
            String campos = "";
            for (ValidadorCampo c : validador.getCampos()) {
                if (!c.isCampoValido()) {
                    campos += ", " + c.getNomeCampo();
                }
            }
            campos += ".";
            campos = campos.substring(2);

            JOptionPane.showMessageDialog(null, "Por favor, verifique os campos: " + campos, "Aviso", JOptionPane.OK_OPTION);
            return;
        }

        if (!txtSenha.getText().equals(txtConfirmacaoSenha.getText())) {
            JOptionPane.showMessageDialog(null, "Por favor, verifique a senha, pois não confere com a Confirmação!", "Aviso", JOptionPane.OK_OPTION);
            return;
        }

        boolean novo = conta == null;

        if (cbTipoConta.getSelectedItem().toString().equals("Poupanca")) {

            Float taxaRendimento = 0F;
            try {
                taxaRendimento = decimalFormat.parse(txtTaxaRendimento.getText().trim()).floatValue();
            } catch (Exception e) {
            }
            if (taxaRendimento == 0F) {
                JOptionPane.showMessageDialog(null, "Por favor, verifique o valor da Taxa de Rendimento da Conta!", "Aviso", JOptionPane.OK_OPTION);
                return;
            }

            if (conta == null) {
                conta = new ContaPoupanca(titular, taxaRendimento);
            } else {
                ((ContaPoupanca) conta).setTaxaRendimento(taxaRendimento);
            }

        } else if (cbTipoConta.getSelectedItem().toString().equals("Corrente")) {

            Float taxaMensal = 0F;
            try {

                taxaMensal = decimalFormat.parse(txtTaxaMensal.getText().trim()).floatValue();
            } catch (Exception e) {
            }
            if (taxaMensal == 0F) {
                JOptionPane.showMessageDialog(null, "Por favor, verifique o valor da Taxa de Mensal da Conta!", "Aviso", JOptionPane.OK_OPTION);
                return;
            }
            if (conta == null) {
                conta = new ContaCorrente(titular, taxaMensal);
            } else {
                ((ContaCorrente) conta).setTaxaMensal(taxaMensal);
            }

        } else {

            Float taxaMensal = 0F;
            try {
                taxaMensal = decimalFormat.parse(txtTaxaMensal.getText().trim()).floatValue();
            } catch (Exception e) {
            }
            if (taxaMensal == 0F) {
                JOptionPane.showMessageDialog(null, "Por favor, verifique o valor da Taxa de Mensal da Conta!", "Aviso", JOptionPane.OK_OPTION);
                return;
            }

            Float taxaJuros = 0F;
            try {
                taxaJuros = decimalFormat.parse(txtTaxaJuros.getText().trim()).floatValue();
            } catch (Exception e) {
            }
            if (taxaJuros == 0F) {
                JOptionPane.showMessageDialog(null, "Por favor, verifique o valor da Taxa de Juros da Conta!", "Aviso", JOptionPane.OK_OPTION);
                return;
            }

            Float limiteConta = 0F;
            try {
                limiteConta = decimalFormat.parse(txtLimiteConta.getText().trim()).floatValue();
            } catch (Exception e) {
            }
            if (limiteConta == 0F) {
                JOptionPane.showMessageDialog(null, "Por favor, verifique o valor do Limite da Conta!", "Aviso", JOptionPane.OK_OPTION);
                return;
            }

            if (conta == null) {
                conta = new ContaEspecial(titular, taxaMensal, taxaJuros, limiteConta);
            } else {
                ((ContaEspecial) conta).setTaxaMensal(taxaMensal);
            }
            ((ContaEspecial) conta).setTaxaJuros(taxaJuros);
            ((ContaEspecial) conta).setLimiteConta(limiteConta);

        }

        if (!(novo || conta.getSenha().equals(txtSenha.getText()))) {
            Contexto.log("cadastroContas", "Senha de Conta Alterada " + txtAgencia.getText() + "/" + txtConta.getText());
        }

        conta.setAgencia(txtAgencia.getText());
        conta.setNumeroConta(txtConta.getText());
        conta.setSenha(txtSenha.getText());

        if (novo) {
            Contexto.getColecaoContas().put(conta.toString(), conta);
            JOptionPane.showMessageDialog(null, "Conta Adicionada com Sucesso!", "Ok", JOptionPane.OK_OPTION);
            Contexto.log("cadastroContas", "Conta Adicionada " + txtAgencia.getText() + "/" + txtConta.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Conta Alterada com Sucesso!", "Ok", JOptionPane.OK_OPTION);
            Contexto.log("cadastroContas", "Conta Alterada " + txtAgencia.getText() + "/" + txtConta.getText());
        }

        frmCadastro.buscarContas();

        btnCancelarActionPerformed(null);
    }//GEN-LAST:event_btnSalvarActionPerformed

