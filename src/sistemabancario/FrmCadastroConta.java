    public void buscarContas() {
        modelContas.setNumRows(0);

        Contexto.getColecaoContas().forEach((k, c) -> {
            if (c.getTitular().equals(titular)) {

                modelContas.addRow(new Object[]{c.getAgencia(), c.getNumeroConta(), c.getTipoConta(), dateFormat.format(c.getDataAbertura())});
            }
        });
    }

