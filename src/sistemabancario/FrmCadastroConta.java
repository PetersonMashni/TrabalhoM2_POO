    @Override
    public void mousePressed(MouseEvent mouseEvent) {
        JTable table = (JTable) mouseEvent.getSource();
        Point point = mouseEvent.getPoint();
        int row = table.rowAtPoint(point);
        if (mouseEvent.getClickCount() == 2 && table.getSelectedRow() != -1) {
            for (Conta conta : titular.getContas()) {
                if (conta.getAgencia().equals(table.getModel().getValueAt(row, 0).toString())
                        && conta.getNumeroConta().equals(table.getModel().getValueAt(row, 1).toString())) {
                    new FrmEditaConta(frmPrincipal, this, titular, conta);
                }
            }
        }
    }

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (cbTipoPessoa.getSelectedIndex() == 0) {
            titular = Contexto.getColecaoPessoasFisicas().get(txtCpfCnpj.getText());
        } else {
            titular = Contexto.getColecaoPessoasJuridicas().get(txtCpfCnpj.getText());
        }
        if (titular == null) {
            JOptionPane.showMessageDialog(null, "Cpf/Cnpj não encontrado !", "Aviso", JOptionPane.OK_OPTION);
            return;
        }

        Contexto.log("cadastroContas", "Consulta às contas da pessoa " + txtCpfCnpj.getText());
        txtNome.setText(titular.getNome());

        buscarContas();
        btnNovaConta.setEnabled(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void cbTipoPessoaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTipoPessoaItemStateChanged
        JComboBox cb = (JComboBox) evt.getSource();

        Object item = evt.getItem();

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (item.toString() == "Física") {
                lblCpfCnpj.setText("CPF:");
                txtCpfCnpj.setFormatterFactory(cpfFormatter);

            } else {
                lblCpfCnpj.setText("CNPJ:");
                txtCpfCnpj.setFormatterFactory(cnpjFormatter);
            }
            txtCpfCnpj.setValue(null);
        }
    }//GEN-LAST:event_cbTipoPessoaItemStateChanged

