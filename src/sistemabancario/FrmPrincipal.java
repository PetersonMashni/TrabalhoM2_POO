    void showForm(Class<?> classForm) {
        for (JInternalFrame f : jdPanel.getAllFrames()) {
            if (f.getClass() == classForm) {
                jdPanel.moveToFront(f);
                try {
                    f.setSelected(true);
                } catch (java.beans.PropertyVetoException e) {
                }
                return;
            }
        }

        try {
            classForm.getConstructor(new Class[]{FrmPrincipal.class}).newInstance(new Object[]{this});
        } catch (Exception e) {
            Contexto.log("showForm", "Erro ao iniciar o formulário " + classForm.getName() + " \n" + e.toString());
        }
    }

    void closeForms() {
        for (JInternalFrame f : jdPanel.getAllFrames()) {
            try {
                f.setClosed(true);
            } catch (PropertyVetoException ex) {
            }
        }
    }

