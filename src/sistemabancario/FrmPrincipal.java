    private void logon() {
        while (!Contexto.usuarioEstaAutenticado()) {
            new FrmLogon().setVisible(true);
        }
        for (ItemMenu i : listaMenu) {
            i.menuItem.setEnabled(
                    Contexto.getFuncionarioLogado().getCargo() == CargoFuncionario.Master
                    | Contexto.getFuncionarioLogado().getCargo() == CargoFuncionario.Gerente
                    | i.acessoCaixa);
        }
    }

    private void menuLogonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuLogonActionPerformed
        Contexto.log("login", "Logout de usuário.");
        closeForms();
        Contexto.setFuncionarioLogado(null);
        logon();
    }//GEN-LAST:event_menuLogonActionPerformed

